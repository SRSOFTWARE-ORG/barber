/**
 * Dicionários brutos (sem merge) por idioma. pt-BR é a fonte da verdade de chaves.
 * Exportado para o teste de paridade de chaves (build check anti-regressão).
 */
export const RAW_DICTS: Record<Lang, Dict> = {
  'pt-BR': ptBR,
  'pt-PT': ptPT,
  es,
  en,
};

export const TRANSLATIONS: Record<Lang, Dict> = {
  'pt-BR': ptBR,
  'pt-PT': { ...ptBR, ...ptPT },
  es: { ...ptBR, ...es },
  en: { ...ptBR, ...en },
};

/** Conjunto de idiomas efetivamente suportados pelo app. */
export const SUPPORTED_LANGS: readonly Lang[] = ['pt-BR', 'pt-PT', 'es', 'en'];

/** Fallback seguro quando o idioma é desconhecido/não suportado. */
export const FALLBACK_LANG: Lang = 'en';

/**
 * Normaliza qualquer valor (perfil, localStorage, header) para um idioma suportado.
 * - Casa exatamente (pt-BR, pt-PT, es, en).
 * - Casa por prefixo tolerante (pt, pt_BR, es-ES, en-US, EN...).
 * - Qualquer coisa não reconhecida cai em `en` (fallback seguro).
 */
export function normalizeLang(value: unknown): Lang {
  if (typeof value !== 'string') return FALLBACK_LANG;
  const raw = value.trim();
  if (!raw) return FALLBACK_LANG;
  // Match exato primeiro.
  const exact = SUPPORTED_LANGS.find((l) => l.toLowerCase() === raw.toLowerCase());
  if (exact) return exact;
  // Normaliza separadores e tenta por prefixo de idioma/região.
  const norm = raw.toLowerCase().replace('_', '-');
  if (norm === 'pt' || norm.startsWith('pt-br')) return 'pt-BR';
  if (norm.startsWith('pt-pt') || norm.startsWith('pt-')) return 'pt-PT';
  if (norm.startsWith('es')) return 'es';
  if (norm.startsWith('en')) return 'en';
  return FALLBACK_LANG;
}

/** Traduz uma chave para o idioma informado, com fallback pt-BR. */
export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const safeLang = normalizeLang(lang);
  let str = TRANSLATIONS[safeLang]?.[key] ?? TRANSLATIONS['pt-BR'][key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
    }
  }
  return str;
}

